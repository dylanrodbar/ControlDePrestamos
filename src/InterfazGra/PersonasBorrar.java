    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(Texto1.getText().equals("") || Texto2.getText().equals("") || Texto3.getText().equals(""))
        JOptionPane.showMessageDialog(this, "No ha seleccionado una persona", "Error", 0);
        else
        {
            con.eliminarPersona(Combo.getSelectedIndex()-1); //Manda mensaje para eliminar persona
            JOptionPane.showMessageDialog(this, "Se ha eliminado a la persona exitosamente", "Informaci√≥n", 1);
            PersonasBorrar abrir = new PersonasBorrar ();
            abrir.setVisible(true);
            this.setVisible(false);
            //Abrir de nuevo la ventana
        }
    }//GEN-LAST:event_jButton2ActionPerformed

