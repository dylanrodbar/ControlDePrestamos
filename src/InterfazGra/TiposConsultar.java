    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        tipos = con.devolverArrayListTipos();
        Combo.addItem("------");
        for(String e: tipos)
        {
            Combo.addItem(e);
        }
    }//GEN-LAST:event_formWindowOpened

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(Combo.getSelectedIndex()==0)
        JOptionPane.showMessageDialog(this, "No ha seleccionado un tipo", "Error", 0);
        else
        {
            Tipos temporal = con.devolverTipo(Combo.getSelectedIndex()-1);
            items = temporal.devolverArrayListItems();
            String cadena = "";
            for(String e: items)
            {
                cadena+=e;
            }
            Texto1.setText(temporal.getNombre());
            Texto2.setText(cadena);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

